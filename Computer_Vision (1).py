import os
import time
import random
import shutil
import torch
import torch.nn as nn
import torch.optim as optim
from torchvision import datasets, transforms, models
from torch.utils.data import DataLoader, Subset
from sklearn.model_selection import KFold
import numpy as np

# --- Rastgeleliği (Randomness) sağladık resimlerin benzer sırayla öğrenilebilmesi için. (for randomness control) ---
def set_seed(seed=42):
    random.seed(seed)
    os.environ['PYTHONHASHSEED'] = str(seed)
    np.random.seed(seed)
    torch.manual_seed(seed)
    torch.cuda.manual_seed(seed)
    torch.cuda.manual_seed_all(seed)
    torch.backends.cudnn.deterministic = True
    torch.backends.cudnn.benchmark = False

set_seed(42)


if os.path.exists('/content/data'):
    DATA_DIR = '/content/data'
else:
    DATA_DIR = './data'

print(f"Secilen Veri Yolu: {DATA_DIR}")

# --- TEMİZLİKÇİ: .ipynb_checkpoints SİLİCİ kendi bilgisayarımızda boyle bir hata aldığımız için ekledik hocam sizdede bu hata tekrarlanmasın diye  ---
